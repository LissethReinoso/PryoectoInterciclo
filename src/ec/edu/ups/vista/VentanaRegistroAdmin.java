    public void cargarDatos() {
        modelo.setRowCount(0);
        for (Administrador ad : controladorParqueadero.administradores()) {
            String datos[] = {String.valueOf(ad.getCodigo()), ad.getCedula(), ad.getNombre(), ad.getApellido(), ad.getTelefono(), ad.getDireccion(), ad.getCorreo(), ad.getUsuario(), ad.getContrasenia()};
            modelo.addRow(datos);
        }
    }
    
    public void verEnTabla(int posicion) {
        if (posicion >= 0) {
            Administrador administrador = controladorParqueadero.administradores().get(posicion);
            txtCodigo.setText(String.valueOf(administrador.getCodigo()));
            txtCedula.setText(administrador.getCedula());
            txtNombre.setText(administrador.getNombre());
            txtApellido.setText(administrador.getApellido());
            txtTelefono.setText(administrador.getTelefono());
            txtDireccion.setText(administrador.getDireccion());
            txtCorreo.setText(administrador.getCorreo());
            txtUsuarioA.setText(administrador.getUsuario());
            pwdContraseña.setText(administrador.getContrasenia());
        } else {
            limpiar();
        }
    }


