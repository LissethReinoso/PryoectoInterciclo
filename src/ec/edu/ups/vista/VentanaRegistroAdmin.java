    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:

        if (pwdContraseña.getText().equals(pwdConfrimar.getText())) {
            String mensaje;
            try {
                Administrador ad = controladorParqueadero.buscarAdministrador(txtCedula.getText());
                if (ad != null) {
                    JOptionPane.showMessageDialog(this, "El Admin ya ha sido registrado");
                } else {
                    if (controladorParqueadero.administradores().size() > 1 && controladorParqueadero.administradores().size() < 2  ) {
                        JOptionPane.showMessageDialog(this, "Solo puede crear un Admin");
                    } else {
                        mensaje = controladorParqueadero.almacenarAdministrador(txtCedula.getText(), txtNombre.getText(), txtApellido.getText(), txtTelefono.getText(), txtDireccion.getText(), txtCorreo.getText(), txtUsuarioA.getText(), pwdContraseña.getText());
                        JOptionPane.showMessageDialog(this, mensaje);
                        cargarDatos();
                        limpiar();
                        // verEnTabla(-1);
                    }
                }
            } catch (EspacioException | CedulaException ex) {
                JOptionPane.showMessageDialog(this, ex.toString());
            }
        } else {
            JOptionPane.showMessageDialog(this, "La contraseña no coincide ");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

